package hr.fer.zemris.fthesis.util;

public class Rectangle2D {

    public final int x;
    public final int y;
    public final int width;
    public final int height;

    public Rectangle2D(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

}
